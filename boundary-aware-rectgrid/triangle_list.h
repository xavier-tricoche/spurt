double determinant_3x3(double dMatrix[3][3]) {
	double x = ((dMatrix[1][1] * dMatrix[2][2]) - (dMatrix[2][1] * dMatrix[1][2]));
	double y = ((dMatrix[1][0] * dMatrix[2][2]) - (dMatrix[2][0] * dMatrix[1][2]));
	double z = ((dMatrix[1][0] * dMatrix[2][1]) - (dMatrix[2][0] * dMatrix[1][1]));

	return (dMatrix[0][0] * x) - (dMatrix[0][1] * y) + (dMatrix[0][2] * z);
}

