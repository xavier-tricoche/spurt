Eigen::RowVectorXd Knots_with_multiplicity(Eigen::RowVectorXd initial_knot, int degree)
{
	Eigen::RowVectorXd knots_mul(initial_knot.size() + 2 * degree);
	for (int i = 0; i < knots_mul.size(); i++)
		if (i < degree) knots_mul(i) = initial_knot(0);
		else if (i >= degree && i <= knots_mul.size() - degree - 1)
			knots_mul(i) = initial_knot(i - degree);
		else if (i > initial_knot.size() + degree - 1)
			knots_mul(i) = initial_knot(initial_knot.size() - 1);
		else
			cout << "something is wrong";
	return knots_mul;
}

