template<typename T1, typename T2>
void grid_to_cube_vertex_reordering(T1* array, const int arr_size, T2* ordered_array)
{
    int order[8] = { 2,3,1,0,6,7,5,4 };
    for (int i = 0; i < 8; i++) {
        ordered_array[i] = array[order[i]];
    }
}

